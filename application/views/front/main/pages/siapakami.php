    <p>
        Borneo Climate merupakan jaringan kerja masyarakat sipil di Kalimantan
Tengah untuk mendorong keterlibatan warga dan masyarakat adat dalam
perencanaan, perumusan, pelaksanaan, dan pemantauan program
pengurangan emisi karbon dan pencegahan kerusakan hutan atau dikenal
dengan istilah REDD+. Pelaksanaan program REDD+ harus memperhatikan
pendapat dan aspirasi warga dan masyarakat adat sebagai komunitas yang
bersentuhan langsung dengan kelestarian hutan.
    </p>
    <p>
        Sistem informasi Borneo Climate didukung oleh Mitra 1.0. Mitra 1.0
merupakan aplikasi sumber terbuka (open source) yang melayani
pengelolaan informasi berbasis SMS (short messeage service), web 2.0,
dan data geospasial. Sistem ini memungkin warga yang memiliki telepon
seluler bisa mengirimkan gagasan, masukan, dan laporan terkait dengan
peristiwa-peristiwa di lapangan secara langsung.
    </p>
    <p> 
Borneo Climate berharap sistem ini dapat membangun komunikasi
multipihak yang setara dan harmonis. Setiap permasalahan dapat
didiskusikan oleh banyak kalangan sehingga mendapat jalan keluar yang
terbaik.
    </p>    
